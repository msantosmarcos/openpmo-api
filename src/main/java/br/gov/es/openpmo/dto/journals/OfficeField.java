package br.gov.es.openpmo.dto.journals;

public class OfficeField {

  private Long id;

  private String name;

  public Long getId() {
    return this.id;
  }

  public void setId(final Long id) {
    this.id = id;
  }

  public String getName() {
    return this.name;
  }

  public void setName(final String name) {
    this.name = name;
  }

}
